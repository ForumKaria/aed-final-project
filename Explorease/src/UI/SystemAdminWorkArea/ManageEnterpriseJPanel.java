    public void populateTable() {
        entTable.setRowCount(0);
        ArrayList<Enterprise> en = this.platform.getEnterpriseDirectory().getEnterpriseList();
        if (en.size() > 0) {
            for ( Enterprise e: en) {
                for (UserAccount usera: e.getUserAccountDirectory().getUseraccountList()){
                    
                    Object[] row = new Object[4];
                    row[0] = e.getClass().getSimpleName();
                    row[1] = usera.getAccountId();
                    row[2] = usera.getUsername();
                    row[3] = usera.getPassword(); 
                    entTable.addRow(row);
                }
                
            }
        }
    }
