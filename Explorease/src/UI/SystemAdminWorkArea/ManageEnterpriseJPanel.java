    private void addOrgnManagerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addOrgnManagerBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = ent.getSelectedRow();
        if(selectedRow>=0){
            Enterprise entSelected = this.platform.getEnterpriseDirectory().findEnterpriseByType((String) ent.getValueAt(selectedRow, 0));
        
            String name = managerName.getText();
            String user = username.getText();
            String pass = password.getText();
            
            VerifyNull checkNull = new VerifyNull();
            boolean nonull = checkNull.checkNullObject(name,user,pass);
            if(nonull){
//            UserAccount userCreated = entSelected.getUserAccountDirectory().findById(user);
            Boolean usernameUnique = this.platform.getEnterpriseDirectory().checkUsernameUniqueAcrossEnts(user);
            if (usernameUnique) {
                UserAccount userCreated = entSelected.getUserAccountDirectory().createUserAccount(user, pass, new EnterpriseAdminRole());
                Person entManagerPerson = this.platform.getPersonDirectory().createPerson(userCreated.getAccountId(), managerName.getText());
                
                populateTable();
            }else {
                JOptionPane.showMessageDialog(null, "Username taken!");
            }}
        } else {
            JOptionPane.showMessageDialog(null, "Select an Enterprise!");

        }
        

    }//GEN-LAST:event_addOrgnManagerBtnActionPerformed

    private void updateOrgnManagerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateOrgnManagerBtnActionPerformed
        // TODO add your handling code here:

        int selectedRow = ent.getSelectedRow();

        if (selectedRow > -1) {
            
            VerifyNull checkNull = new VerifyNull();
            boolean nonull = checkNull.checkNullObject(updatePass.getText());
        
            if(nonull){
            Enterprise entSelected = this.platform.getEnterpriseDirectory().findEnterpriseByType((String) ent.getValueAt(selectedRow, 0));
            String username = (String) ent.getValueAt(selectedRow, 2);

            UserAccount uaSelected = entSelected.getUserAccountDirectory().findByUserName(username);
            uaSelected.setPassword(updatePass.getText());

            populateTable();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Select an Enterprise!");

        }
    }//GEN-LAST:event_updateOrgnManagerBtnActionPerformed

    private void deleteOrgManagerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteOrgManagerActionPerformed
        // TODO add your handling code here:

        int selectedRow = ent.getSelectedRow();

        if (selectedRow > -1) {
            Enterprise entSelected = this.platform.getEnterpriseDirectory().findEnterpriseByType((String) ent.getValueAt(selectedRow, 0));
            String username = (String) ent.getValueAt(selectedRow, 2);

            
            UserAccount uaSelected = entSelected.getUserAccountDirectory().findByUserName(username);
            entSelected.getUserAccountDirectory().getUseraccountList().remove(uaSelected);

            populateTable();

        } else {
            JOptionPane.showMessageDialog(null, "Select an Enterprise!");

        }
    }//GEN-LAST:event_deleteOrgManagerActionPerformed

